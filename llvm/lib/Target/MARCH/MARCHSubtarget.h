#ifndef LLVM_LIB_TARGET_MARCH_MARCHSUBTARGET_H
#define LLVM_LIB_TARGET_MARCH_MARCHSUBTARGET_H

#include "MARCHFrameLowering.h"
#include "MARCHISelLowering.h"
#include "MARCHInstrInfo.h"
#include "llvm/CodeGen/SelectionDAGTargetInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "MARCHGenSubtargetInfo.inc"

namespace llvm {

class StringRef;
class TargetMachine;

class MARCHSubtarget : public MARCHGenSubtargetInfo {
  virtual void anchor();

  MARCHRegisterInfo RegInfo;
  MARCHInstrInfo InstrInfo;
  MARCHFrameLowering FrameLowering;
  MARCHTargetLowering TLInfo;
  SelectionDAGTargetInfo TSInfo;

public:
  MARCHSubtarget(const Triple &TT, const std::string &CPU, const std::string &FS,
                const TargetMachine &TM);

  /// Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef TuneCPU, StringRef FS);

  const MARCHInstrInfo *getInstrInfo() const override { return &InstrInfo; }
  const MARCHFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const MARCHTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const MARCHRegisterInfo *getRegisterInfo() const override { return &RegInfo; }
  const SelectionDAGTargetInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
};

} // end namespace llvm

#endif // LLVM_LIB_TARGET_MARCH_MARCHSUBTARGET_H
